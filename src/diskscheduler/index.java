    private void headCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_headCaretUpdate
       int i =0 , j=0;
        if(!head.getText().equals(""))
            i = Integer.parseInt(head.getText());
            j = Integer.parseInt(cyl.getText());
        if(i>0 && i<=j){
            reqSeq.setEnabled(true);
            head.setForeground(Color.black);
        }
        else{
            reqSeq.setEnabled(false);
            if(i>j){
                head.setForeground(Color.red);
            }
        }
        
        
    }//GEN-LAST:event_headCaretUpdate

