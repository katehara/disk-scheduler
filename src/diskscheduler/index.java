    private void goBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goBtnActionPerformed
        String lines[] = reqSeq.getText().split("\\s+");
        int c = Integer.parseInt(cyl.getText());
        int h = Integer.parseInt(head.getText());
        boolean work = true;
        int i = lines.length;
        int seq[] = new int[i];
        for(int j = 0; j<i;j++){
            
            seq[j]= Integer.parseInt(lines[j]);
            if(seq[j]>c || seq[j]<0){
                reqSeq.setForeground(Color.red);
                work = false;
            }
            else {
                reqSeq.setForeground(Color.black);
            }
        }
        
        if(work){
            DiskScheduling ds = new DiskScheduling(seq , c , h);
            u = ds.res1.output;
            uc = ds.res1.chart;
            ul = ds.res1.cnt;
            v = ds.res2.output;
            vc = ds.res2.chart;
            vl = ds.res2.cnt;
            w = ds.res3.output;
            wc = ds.res3.chart;
            wl = ds.res3.cnt;
            r = ds.res4.output;
            rc = ds.res4.chart;
            rl = ds.res4.cnt;
            s = ds.res5.output;
            sc = ds.res5.chart;
            sl = ds.res5.cnt;
            z = ds.res6.output;
            zc = ds.res6.chart;
            zl = (int)ds.res6.cnt2;
            r1.setText(""+ul);
            r2.setText(""+vl);
            r3.setText(""+wl);
            r4.setText(""+rl);
            r5.setText(""+sl);
            r6.setText(""+zl);
            setResult(u);
            uc.setVisible(true);
            setBest(ul , vl , wl , rl , sl , zl);
        }
        else
        {
            result.setText("Invalid Request values!!!");
        }
        
        
    }//GEN-LAST:event_goBtnActionPerformed

