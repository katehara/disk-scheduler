    public void setBest(int a , int b , int c , int d , int e , int f){
        if (a<=b && a<=c && a<=d && a<=e && a<=f) result.setText("FCFS is best");
        else if (b<=a && b<=c && b<=d && b<=e && b<=f) result.setText("SSTF is best");
        else if (c<=b && c<=a && c<=d && c<=e && c<=f) result.setText("SCAN is best");
        else if (d<=b && d<=c && d<=a && d<=e && d<=f) result.setText("CSCAN is best");
        else if (e<=b && e<=c && e<=d && e<=a && e<=f) result.setText("LOOK is best");
        else if (f<=b && f<=c && f<=d && f<=e && f<=a) result.setText("CLOOK is best");
    
    }

